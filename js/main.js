const $ = (selector, parent = document) => parent.querySelector(selector);
const $$ = (selector, parent = document) => [...parent.querySelectorAll(selector)];

function setLanguage(lang) {
    const safeLang = translations[lang] ? lang : "pt";

    localStorage.setItem("lang", safeLang);

    $$("[data-i18n]").forEach((el) => {
        const key = el.dataset.i18n;
        const value = translations[safeLang][key];

        if (value) {
            el.textContent = value;
        }
    });

    document.documentElement.lang = safeLang === "pt" ? "pt-BR" : "es-ES";

    $$("[data-lang]").forEach((btn) => {
        const isActive = btn.dataset.lang === safeLang;
        btn.classList.toggle("is-active", isActive);
        btn.setAttribute("aria-pressed", String(isActive));
    });
}

function initMenu() {
    const menuButton = $(".menu-toggle");
    const navLinks = $("#navLinks");

    if (!menuButton || !navLinks) return;

    const closeMenu = () => {
        navLinks.classList.remove("active");
        menuButton.setAttribute("aria-expanded", "false");
        document.body.classList.remove("menu-open");
    };

    const openMenu = () => {
        navLinks.classList.add("active");
        menuButton.setAttribute("aria-expanded", "true");
        document.body.classList.add("menu-open");
    };

    menuButton.addEventListener("click", () => {
        navLinks.classList.contains("active") ? closeMenu() : openMenu();
    });

    $$(".nav-links a").forEach((link) => {
        link.addEventListener("click", closeMenu);
    });

    document.addEventListener("keydown", (event) => {
        if (event.key === "Escape") {
            closeMenu();
        }
    });

    document.addEventListener("click", (event) => {
        if (!navLinks.classList.contains("active")) return;

        if (!navLinks.contains(event.target) && !menuButton.contains(event.target)) {
            closeMenu();
        }
    });
}

function initReveal() {
    const revealItems = $$(".reveal");

    if (!("IntersectionObserver" in window)) {
        revealItems.forEach((el) => el.classList.add("active"));
        return;
    }

    const observer = new IntersectionObserver((entries, obs) => {
        entries.forEach((entry) => {
            if (!entry.isIntersecting) return;

            entry.target.classList.add("active");
            obs.unobserve(entry.target);
        });
    }, {
        threshold: 0.14,
        rootMargin: "0px 0px -40px 0px"
    });

    revealItems.forEach((el) => observer.observe(el));
}

function initScrollEffects() {
    const navbar = $("[data-navbar]");
    const hero = $(".hero");
    const backToTop = $(".back-to-top");
    const sections = $$("main section[id]");
    const links = $$(".nav-links a[href^='#']");
    const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

    let ticking = false;

    const update = () => {
        const scrollTop = window.scrollY || document.documentElement.scrollTop;
        const docHeight = document.documentElement.scrollHeight - window.innerHeight;
        const progress = docHeight > 0 ? Math.min(scrollTop / docHeight, 1) : 0;

        document.body.style.setProperty("--scroll", progress.toFixed(4));

        if (navbar) {
            navbar.classList.toggle("is-scrolled", scrollTop > 12);
        }

        if (backToTop) {
            backToTop.classList.toggle("is-visible", scrollTop > 520);
        }

        if (hero && !reduceMotion) {
            hero.style.setProperty("--hero-parallax", `${Math.min(scrollTop * 0.16, 90)}px`);
        }

        let current = "";

        sections.forEach((section) => {
            if (scrollTop >= section.offsetTop - 130) {
                current = section.id;
            }
        });

        links.forEach((link) => {
            const hash = link.getAttribute("href").replace("#", "");
            link.classList.toggle("is-active", hash === current);
        });

        ticking = false;
    };

    const requestUpdate = () => {
        if (ticking) return;

        window.requestAnimationFrame(update);
        ticking = true;
    };

    window.addEventListener("scroll", requestUpdate, { passive: true });
    window.addEventListener("resize", requestUpdate);

    if (backToTop) {
        backToTop.addEventListener("click", () => {
            window.scrollTo({
                top: 0,
                behavior: reduceMotion ? "auto" : "smooth"
            });
        });
    }

    update();
}

function initLightbox() {
    const gallery = $("[data-gallery]");

    if (!gallery) return;

    const lightbox = document.createElement("div");
    lightbox.className = "lightbox";
    lightbox.innerHTML = '<button type="button" aria-label="Fechar imagem">×</button><img alt="">';

    document.body.appendChild(lightbox);

    const lightboxImg = $("img", lightbox);
    const closeButton = $("button", lightbox);

    const close = () => {
        lightbox.classList.remove("is-open");
        document.body.classList.remove("menu-open");
        lightboxImg.removeAttribute("src");
    };

    gallery.addEventListener("click", (event) => {
        const img = event.target.closest("img");

        if (!img) return;

        lightboxImg.src = img.src;
        lightboxImg.alt = img.alt || "Imagem ampliada";
        lightbox.classList.add("is-open");
        document.body.classList.add("menu-open");
    });

    closeButton.addEventListener("click", close);

    lightbox.addEventListener("click", (event) => {
        if (event.target === lightbox) {
            close();
        }
    });

    document.addEventListener("keydown", (event) => {
        if (event.key === "Escape") {
            close();
        }
    });
}

function initSwipeDrag() {
    $$("[data-swipe], [data-gallery]").forEach((slider) => {
        let isDown = false;
        let startX = 0;
        let scrollLeft = 0;

        slider.addEventListener("pointerdown", (event) => {
            if (window.innerWidth > 900) return;

            isDown = true;
            startX = event.clientX;
            scrollLeft = slider.scrollLeft;

            if (slider.setPointerCapture) {
                slider.setPointerCapture(event.pointerId);
            }
        });

        slider.addEventListener("pointermove", (event) => {
            if (!isDown) return;

            slider.scrollLeft = scrollLeft - (event.clientX - startX);
        });

        ["pointerup", "pointercancel", "pointerleave"].forEach((type) => {
            slider.addEventListener(type, () => {
                isDown = false;
            });
        });
    });
}

function initTiltCards() {
    const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
    const isTouch = window.matchMedia("(hover: none)").matches;

    if (reduceMotion || isTouch) return;

    const cards = $$(".product-card, .card, .social-card");

    cards.forEach((card) => {
        card.classList.add("tilt-card");

        card.addEventListener("mousemove", (event) => {
            const rect = card.getBoundingClientRect();
            const x = event.clientX - rect.left;
            const y = event.clientY - rect.top;

            const rotateY = ((x / rect.width) - 0.5) * 8;
            const rotateX = (0.5 - (y / rect.height)) * 8;

            card.style.transform = `perspective(900px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) translateY(-6px)`;
        });

        card.addEventListener("mouseleave", () => {
            card.style.transform = "";
        });
    });
}

function initButtonMagnet() {
    const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
    const isTouch = window.matchMedia("(hover: none)").matches;

    if (reduceMotion || isTouch) return;

    $$(".magnetic, .btn-outline-whatsapp, .btn-ghost").forEach((button) => {
        button.addEventListener("mousemove", (event) => {
            const rect = button.getBoundingClientRect();
            const x = event.clientX - rect.left - rect.width / 2;
            const y = event.clientY - rect.top - rect.height / 2;

            button.style.transform = `translate(${x * 0.08}px, ${y * 0.12}px) translateY(-2px)`;
        });

        button.addEventListener("mouseleave", () => {
            button.style.transform = "";
        });
    });
}

document.addEventListener("DOMContentLoaded", () => {
    setLanguage(localStorage.getItem("lang") || "pt");

    $$("[data-lang]").forEach((button) => {
        button.addEventListener("click", () => {
            setLanguage(button.dataset.lang);
        });
    });

    initMenu();
    initReveal();
    initScrollEffects();
    initLightbox();
    initSwipeDrag();
    initTiltCards();
    initButtonMagnet();
});